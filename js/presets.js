// Function to get the reverse mapping (name to note)
function getInverseMapping(preset) {
    const inverseMap = {};
    const mapping = DRUM_PRESETS[preset].mapping;
    
    for (const [noteNum, drumName] of Object.entries(mapping)) {
        inverseMap[drumName] = parseInt(noteNum);
    }
    
    return inverseMap;
}

